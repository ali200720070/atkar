# ===== إعداد السجلات =====
logging.basicConfig(level=logging.INFO)
logger = logging.getLogger("azkar_bot")

# ===== حالة كل دردشة =====
chat_states = {chat_id: 0 for chat_id in CHAT_IDS}
last_messages = {}

# ===== إنشاء العميل =====
client = TelegramClient('azkar_bot_session', API_ID, API_HASH).start(bot_token=BOT_TOKEN)

# ===== حلقة إرسال الأذكار =====
async def send_azkar_loop():
    while True:
        for chat_id in CHAT_IDS:
            index = chat_states[chat_id]
            text = AZKAR_LIST[index]
            try:
                # حذف الرسالة السابقة
                if chat_id in last_messages:
                    await client.delete_messages(chat_id, last_messages[chat_id])
            except Exception:
                pass
            try:
                msg = await client.send_message(chat_id, f"📿 ذكر ودعاء\n\n{text}")
                last_messages[chat_id] = msg.id
                chat_states[chat_id] = (index + 1) % len(AZKAR_LIST)
            except Exception as e:
                logger.error(f"خطأ عند إرسال ذكر لـ {chat_id}: {e}")
        await asyncio.sleep(30)  # للتجربة، يمكن تغييره للساعات

# ===== أمر /start =====
@client.on(events.NewMessage(pattern='/start'))
async def start_handler(event):
    await event.reply("أهلاً بك! سيتم إرسال الأذكار تلقائيًا لكل القروبات المحددة.")

# ===== الدالة الرئيسية =====
async def main():
    logger.info("تشغيل بوت الأذكار باستخدام Telethon")
    asyncio.create_task(send_azkar_loop())
    await client.run_until_disconnected()

if __name__ == "__main__":
    asyncio.run(main())
